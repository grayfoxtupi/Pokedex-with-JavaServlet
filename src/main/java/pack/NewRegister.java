package pack;

public class NewRegister {
	
	private String nome;
	private String img;
	private String tipo;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		tipo = tipo;
	}

}
